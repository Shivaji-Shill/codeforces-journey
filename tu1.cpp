#include <iostream> //Include the standard input-output library (need for cout)

int main() // Main function: the entry point of the program

{
    std::cout << "Hello World";
    //std::cout prints the text to the console
    //"Hello World" is the text that will appear on the screen
    // std:: means we are using 'cout' from the standard namespace
    // cout = console output → displays text on the screen
    // "<<" → indicate output
    // ">>" → indicate input

    return 0;
    // return 0 indicates that the program ended successfully
}